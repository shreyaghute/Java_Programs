package conditionalStatements;

public class IfElseIfClass {

	public static void main(String[] args) {
				
				int a = 25;
				
				if(a <= 10) {
					System.out.println(a+ " is between 0 - 10.");
				}
				else if(a <= 20) {
					System.out.println(a+ " is between 11 - 20.");
				}
				else if(a <= 30) {
					System.out.println(a+ " is between 21 - 30.");
				}
				else {
					System.out.println(a+ " is greater than 30");
				}
				
	}

}
